 class UbicacionModel {
    constructor(
        country,
        regionName,
        city,
        lat,
        lng,
        postalCode,
        timezone,
        geonameId,
    ){
        this.country = country;
        this.regionName = regionName;
        this.city = city;
        this.lat = lat;
        this.lng = lng;
        this.postalCode = postalCode;
        this.timezone = timezone;
        this.geonameId = geonameId;
    }
}

